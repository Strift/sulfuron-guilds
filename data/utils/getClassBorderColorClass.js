export default function (classValue) {
  switch (classValue) {
    case 'druid':
      return 'border-druid'
    case 'hunter':
      return 'border-hunter'
    case 'mage':
      return 'border-mage'
    case 'paladin':
      return 'border-paladin'
    case 'priest':
      return 'border-priest'
    case 'rogue':
      return 'border-rogue'
    case 'shaman':
      return 'border-shaman'
    case 'warlock':
      return 'border-warlock'
    case 'warrior':
      return 'border-warrior'
  }
}
